function to_red ($msg) {
    return "$([char]0x1b)[91m$msg$([char]0x1b)[0m"
}

function to_green ($msg) {
    return "$([char]0x1b)[92m$msg$([char]0x1b)[0m"
}

function to_yellow ($msg) {
    return "$([char]0x1b)[93m$msg$([char]0x1b)[0m"
}

function to_blue ($msg) {
    return "$([char]0x1b)[94m$msg$([char]0x1b)[0m"
}

function to_purple ($msg) {
    return "$([char]0x1b)[95m$msg$([char]0x1b)[0m"
}
   
function to_cyan ($msg) {
    return "$([char]0x1b)[96m$msg$([char]0x1b)[0m"
}

function to_white ($msg) {
    return "$([char]0x1b)[97m$msg$([char]0x1b)[0m"
}

function to_lightgrey ($msg) {
    return "$([char]0x1b)[98m$msg$([char]0x1b)[0m"
}





